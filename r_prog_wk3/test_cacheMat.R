#test time difference between first instance
#and second instance calculation
test_out = function(mat){
  tmp <- makeCacheMatrix(mat)
  time_ini <- Sys.time()
  cacheSolve(tmp)
  dur <- Sys.time() - time_ini
  print(dur)
  
  time_ini <- Sys.time()
  cacheSolve(tmp)
  dur <- Sys.time() - time_ini
  print(dur)
}

